"""Pure-string renderers for the generated React project files.

These functions take validated data and return file contents. They never touch
the filesystem — keeping them pure makes them easy to unit-test.
"""

from __future__ import annotations

import json
from collections.abc import Iterable  # noqa: TC003

from appio_shared.schemas import (  # noqa: TC001
    AppSpec,
    BooleanProp,
    Component,
    NumberProp,
    Page,
    StringListProp,
    StringProp,
)

__all__ = [
    "render_app_tsx",
    "render_component_tsx",
    "render_index_html",
    "render_manifest_json",
    "render_package_json",
]


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


def _js_identifier(name: str) -> str:
    """Sanitise a string into a valid JS identifier (caller has already
    validated PascalCase / camelCase via Pydantic)."""
    return "".join(c for c in name if c.isalnum() or c == "_") or "Component"


def _props_literal(props: list[object]) -> str:
    """Render a list of PropEntry items as a JS object literal.

    The runtime component sees ``props`` as a plain object (e.g.
    ``{groupBy: "date", showTrend: true}``) regardless of how the spec
    encoded the typed key/value list.
    """
    if not props:
        return "{}"
    obj: dict[str, object] = {}
    for p in props:
        if isinstance(p, (StringProp, NumberProp, BooleanProp, StringListProp)):
            obj[p.key] = p.value
    return json.dumps(obj, ensure_ascii=False, separators=(", ", ": "))


def _route_to_segment(route: str) -> str:
    if route == "/":
        return "home"
    return route.strip("/").replace("/", "-").replace("-", "_") or "home"


def _unique_component_types(spec: AppSpec) -> list[str]:
    seen: list[str] = []
    seen_set: set[str] = set()
    for page in spec.pages:
        for comp in page.components:
            if comp.type not in seen_set:
                seen.append(comp.type)
                seen_set.add(comp.type)
    return seen


